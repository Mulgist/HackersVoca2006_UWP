                SqliteCommand selectCommand = new SqliteCommand("SELECT * FROM Vocabularies WHERE Day = @Day", db);
                selectCommand.Parameters.AddWithValue("@Day", day);

                SqliteDataReader query = selectCommand.ExecuteReader();

                while (query.Read())
